export function t(lang: Lang, key: keyof typeof DICT, vars?: Record<string, string | number>): string {
  const template = DICT[key]?.[lang] ?? DICT[key]?.ja ?? String(key);
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (str, [name, value]) => str.replaceAll(`{${name}}`, String(value)),
    template
  );
}

export function detectDefaultLang(): Lang {
  if (typeof navigator === "undefined") return "ja";
  const code = navigator.language.slice(0, 2).toLowerCase();
  const supported: Lang[] = ["ja", "en", "zh", "ko", "es", "fr", "pt"];
  return (supported.includes(code as Lang) ? code : "en") as Lang;
}
